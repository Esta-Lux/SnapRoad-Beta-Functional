function normalizeIconKey(key: string): string {
  const aliases: Record<string, string> = {
    merge_straight: 'merge_left',
    continue_straight: 'continue',
    turn_uturn: 'uturn',
    fork_keep_left: 'fork_left',
    fork_keep_right: 'fork_right',
    fork_keep_straight: 'fork_right',
    fork_straight: 'fork_right',
    sharp_left: 'turn_sharp_left',
    sharp_right: 'turn_sharp_right',
    notification: 'continue',
  };
  return aliases[key] ?? key;
}

/**
 * Resolve the icon key from raw Mapbox type + modifier.
 */
function resolveIconKey(type: string, modifier: string): string {
  const t = (type || '').toLowerCase().trim().replace(/\s+/g, '_');
  const m = (modifier || '').toLowerCase().trim().replace(/\s+/g, '_');

  if (m) {
    const key = `${t}_${m}`;
    if (ICON_PATHS[key]) return key;

    if (t === 'merge' && m === 'straight') return 'merge_left';
    if (t === 'fork' && m === 'straight') return 'fork_right';

    const simple = m.replace(/^(sharp|slight)_/, '');
    const fallback = `${t}_${simple}`;
    if (ICON_PATHS[fallback]) return fallback;
  }

  if (ICON_PATHS[t]) return t;

  if (m && (!t || t === 'turn' || t === 'continue') && ICON_PATHS[`turn_${m}`]) {
    return `turn_${m}`;
  }

  return 'continue';
}

function resolveToPathKey(type: string, modifier: string): string {
  const raw = resolveIconKey(type, modifier);
  const k = normalizeIconKey(raw);
  if (ICON_PATHS[k]) return k;
  if (ICON_PATHS[raw]) return raw;
  return 'continue';
}

function exitBadgeTextFill(iconColor: string): string {
  const c = iconColor.toLowerCase();
  if (c === '#ffffff' || c === '#fff') return '#000';
  return '#fff';
}

export default React.memo(function ManeuverIcon({
  type = '',
  modifier = '',
  fallbackKind,
  size,
  color,
  exitNumber,
}: Props) {
  const hasRaw = type.trim().length > 0 || modifier.trim().length > 0;
  const raw = hasRaw
    ? { type, modifier }
    : fallbackKind && fallbackKind !== 'unknown'
      ? KIND_TO_RAW[fallbackKind]
      : { type: 'continue', modifier: '' };

  const pathKey = resolveToPathKey(raw.type, raw.modifier);
  const path = ICON_PATHS[pathKey] ?? ICON_PATHS.continue!;

  const isRoundabout =
    pathKey.startsWith('roundabout') || pathKey === 'rotary';

  return (
    <Svg width={size} height={size} viewBox="0 0 20 20" fill="none">
      <Path d={path} fill={color} />
      {isRoundabout && exitNumber != null && exitNumber >= 1 && (
        <G>
          <Circle cx={15} cy={5} r={4.5} fill={color} />
          <SvgText
            x={15}
            y={7}
            textAnchor="middle"
            fontSize={6.5}
            fontWeight="900"
            fill={exitBadgeTextFill(color)}
          >
            {exitNumber}
          </SvgText>
        </G>
      )}
    </Svg>
  );
});
